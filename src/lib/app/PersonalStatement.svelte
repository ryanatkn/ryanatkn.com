<h2>Questions I'm asking:</h2>

<ul>
	<li>
		How can technology improve our lives and ecologies? What roles should technology play to improve
		our thinking, communication, health, and social environments? How can we ensure our efforts
		reduce inequalities? What are our blind spots, and what perspectives are we missing and
		minimizing? What else do we need to realize more of technology's liberatory potential? What are
		the big opportunities and low hanging fruit?
	</li>
	<li>
		How can we design technology that respects and reinforces our personal consent boundaries? Is
		our technology working for our individual and collective benefit? If not, why not, and how can
		we fix it? What false stories are blocking our imaginations?
	</li>
	<li>
		How can we include more people in designing, making, and sharing the building blocks of digital
		mediums? How can we make software systems that afford programming powers in everyone's everyday
		life? How can we further leverage the capabilities of the web and our devices? Where is the web
		going and where should we invest our time and attention to steer it?
	</li>
	<li>
		How can we grow sustainable institutions to protect the things we care about? How can we
		structure and develop organizations to achieve better outcomes while supporting individual
		agency and autonomy? What forms of participation work for the majority of us that aren't highly
		motivated? How can technology help? How can we build robust trusted networks? How can we design
		tools to improve collective decision making and shared understanding? How can we make power
		dynamics in groups more explicit? What does it mean for democratic community governance to have
		desirable dynamics around labor and power? In what ways can we practice low-stakes democracy so
		we're better at it when the stakes are higher?
	</li>
	<li>
		How can we support social systems where participation is positive sum, nonfinancialized, and
		intrinsically motivated? What modes of cooperation are available to us in the various meaningful
		aspects of our lives? Which open source communities are thriving, and where is there room for
		improvement? Who is being systemically excluded from participating, and what are the best
		remedies? What are some optimistic visions of a vibrant and benevolent commons? How can we make
		our communities and worlds more safe, welcoming, fair, fun, and useful?
	</li>
</ul>

